# Loops - A loop is used to repeat a block of code multiple times. Instead of writing the same code again and again, we use loops to do it automatically.

# While Loop - A while loop repeats as long as a condition is true.
# Syntax
"""
while condition:
    block of code  """

# Example
# Start with count equal to 1
count = 1

# Run the loop as long as count is less than or equal to 5
while count <= 5:
    # Print the current value of count
    print("Count is:", count)
    
    # Increase count by 1 after each loop iteration
    count += 1
