# Data Types

# There are 5 types of data types.
# they are Integers, Strings, Float, Boolean and None

name = "Alekhya"  
age = 19        
PI = 3.14        
isAdult = True   
value = None     


print(type(name))      # It shows <class 'str'> which means it is a String data type
print(type(age))       # <class 'int'>  means integer data type
print(type(PI))        # <class 'float'> means Float data type
print(type(isAdult))   # <class 'bool'> means Boolean data type
print(type(value))     # <class 'NoneType'> means None data tyoe