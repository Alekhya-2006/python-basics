# Escape Sequence characters:
'''Sequence of characters after backslash "\" is Escape Sequence characters

Escape Sequence characters comprise of more than  one character 
but represent one character when used within the Strings.
Example:  \n -> newline
          \t -> Tab
          \' -> singlequote
          \\ -> backslash   
          etc...            '''

a = "\"Alekhya\" is a good girl \nbut some time don't listen to her parents"
print(a)

'''
Output:
"Alekhya" is a good girl 
but some time don't listen to her parents   '''