# Conditional Statements - Conditional statements are used to make decisions in your code based on conditions.

# If statement - The if statement is used to run a block of code only if a certain condition is true.
# syntax 
"""
if condition:
    code to run if condition is true   """

# Example
age1 = 18

if age1 >= 18:
    print("You are eligible to vote.")    # since the condition is true, it prints "You are eligible to vote."

age2 = 8
if age2 >= 18:
    print("You are eligible to vote.")    # since the condition is not true, it prints nothing.



# Shorthand if Statement - Used for simple one-line if statements.    
# Syntax - if condition: do_this() 

# Example
x = 7 
if x > 5: print("x is greater than 5")